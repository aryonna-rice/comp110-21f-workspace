"""Practice with relational operators."""

left_side: int = int(input("Choose a #:"))
right_side: int = int(input("another one: "))

print(f"{left_side} < {right_side} is {left_side < right_side}")
print(f"{left_side} >= {right_side} is {left_side >= right_side}")
print(f"{left_side} == {right_side} is {left_side == right_side}")
print(f"{left_side} != {right_side} is {left_side != right_side}")