"""Practice with numeric operators and types."""

left_side: int = int(input("Choose a number: "))
right_side: int = int(input("Choose a number: "))

print(f"{left_side} **  {right_side} is {left_side ** right_side}")
print(f"{left_side} / {right_side} is {left_side / right_side} ")
print(f"{left_side} // {right_side} is {left_side  // right_side}")
print(f"{left_side} % {right_side} is {left_side % right_side}")